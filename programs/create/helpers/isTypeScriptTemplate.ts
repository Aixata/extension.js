import isExternalTemplate from './isExternalTemplate'

export default function isTypeScriptTemplate(template: string) {
  if (isExternalTemplate(template)) {
    return false
  }

  return (
    template === 'typescript' ||
    template.startsWith('typescript-') ||
    template.endsWith('-typescript')
  )
}
